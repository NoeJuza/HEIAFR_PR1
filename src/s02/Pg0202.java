package s02;

public class Pg0202 {
  public static void main(String [] args) {
    int j = 7, k = 4;  boolean a = true, b = false; // 1
    a = 7 >= 3 | (7 != 3);                          // 2
    b = a != true;                                  // 3
    a ^= true;                                      // 4
    a &= false | b | 7 == 3 + 4;                    // 5 (*)
    b = k++ >= 3 || j-- == 4;                       // 6
    a = j != 6 | k++ == 6 && j-- >= 0;              // 7 (*)
  }
}
